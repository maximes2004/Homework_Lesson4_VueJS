// Вы разрабатываете приложение для отображения и сортировки списка товаров. У вас есть массив объектов products, где каждый объект представляет товар с его названием и ценой. Вам необходимо отобразить список товаров и предоставить пользователю возможность сортировать товары по цене (по возрастанию и по убыванию).
new Vue({
  el: "#app", // Привязываем Vue к элементу с id "app"
  data: {
    // Массив товаров с их названиями и ценами
    products: [
      { name: "Товар 1", price: 100 },
      { name: "Товар 2", price: 200 },
      { name: "Товар 3", price: 150 },
      { name: "Товар 4", price: 50 },
      { name: "Товар 5", price: 300 },
    ],
    // Параметр сортировки: 'asc' для сортировки по возрастанию, 'desc' для сортировки по убыванию
    sortOrder: "asc",
  },
  computed: {
    // Вычисляемое свойство для сортировки товаров в зависимости от параметра sortOrder
    sortedProducts() {
      // Создаем копию массива products
      let sorted = this.products.slice();
      // Сортируем массив по возрастанию или убыванию цены в зависимости от sortOrder
      sorted.sort((a, b) => {
        if (this.sortOrder === "asc") {
          return a.price - b.price; // Сортировка по возрастанию
        } else {
          return b.price - a.price; // Сортировка по убыванию
        }
      });
      return sorted; // Возвращаем отсортированный массив
    },
  },
  methods: {
    // Метод для установки параметра сортировки на 'asc'
    sortByPriceAsc() {
      this.sortOrder = "asc";
    },
    // Метод для установки параметра сортировки на 'desc'
    sortByPriceDesc() {
      this.sortOrder = "desc";
    },
  },
});
